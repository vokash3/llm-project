import dill


class BPE:
    """
    Простая реализация алгоритма Byte Pair Encoding.

    BPE обучается так:

    1. Сначала текст разбивается на отдельные символы.
       Например:

       "banana" -> ["b", "a", "n", "a", "n", "a"]

    2. Затем алгоритм ищет самую частую пару соседних токенов.
       Например:

       ("a", "n") встречается чаще всего.

    3. Эта пара объединяется в новый токен:

       ("a", "n") -> "an"

    4. Новый токен добавляется в словарь.

    5. Та же самая пара объединяется во всей последовательности.

    6. Процесс повторяется до тех пор, пока размер словаря
       не достигнет vocab_size.

    После обучения появляются два словаря:

    id2token:
        {0: "a", 1: "b", 2: "n", 3: "an"}

    token2id:
        {"a": 0, "b": 1, "n": 2, "an": 3}

    Также сохраняется список merge-операций.

    Это важно для encode(), потому что при кодировании нового текста
    нужно повторить те же самые объединения в том же самом порядке.
    """

    def __init__(self, vocab_size: int):
        """
        Создаёт объект BPE.

        Parameters
        ----------
        vocab_size : int
            Максимальный размер словаря токенов.

            Например, если vocab_size = 28, то после обучения
            в словаре должно быть не больше 28 токенов.

        Внутри класса сохраняются:

        self.vocab_size:
            желаемый размер словаря.

        self.id2token:
            словарь вида id -> token.

        self.token2id:
            словарь вида token -> id.

        self.merges:
            список merge-операций в порядке обучения.

            Например:

            [
                ("a", "n"),
                ("an", "a"),
                ("b", "ana")
            ]

            Это означает:

            1. сначала объединяли "a" и "n" в "an";
            2. потом "an" и "a" в "ana";
            3. потом "b" и "ana" в "bana".
        """

        self.vocab_size = vocab_size

        # Словарь id -> token.
        # Заполнится после вызова fit().
        self.id2token = {}

        # Словарь token -> id.
        # Заполнится после вызова fit().
        self.token2id = {}

        # Список всех merge-операций.
        #
        # Это ключевая часть для корректной работы encode().
        #
        # Нельзя восстанавливать пару из готового токена через token[:-1]
        # и token[-1], потому что токен мог быть создан из двух
        # уже составных токенов.
        #
        # Например:
        #
        # ("an", "a") -> "ana"
        #
        # Если пытаться восстановить это как:
        #
        # "ana"[:-1] -> "an"
        # "ana"[-1]  -> "a"
        #
        # тут случайно получится правильно.
        #
        # Но в общем случае надёжнее и правильнее хранить настоящую пару,
        # которую выбрал алгоритм во время обучения.
        self.merges = []

    def fit(self, text: str):
        """
        Обучает BPE на переданном тексте.

        Parameters
        ----------
        text : str
            Текстовый корпус для обучения.

        Что делает метод:

        1. Находит все уникальные символы текста.
        2. Сортирует их по алфавиту.
        3. Использует их как начальный словарь токенов.
        4. Разбивает весь текст на отдельные символы.
        5. Много раз:
            - считает частоты соседних пар токенов;
            - выбирает самую частую пару;
            - создаёт из неё новый токен;
            - добавляет новый токен в словарь;
            - сохраняет merge-операцию;
            - объединяет эту пару во всей последовательности.
        6. Создаёт id2token и token2id.

        Важно:
        если несколько пар имеют одинаковую частоту,
        выбирается та, которая встретилась первой.

        Это работает благодаря тому, что dict в Python сохраняет
        порядок добавления элементов.
        """

        # ============================================================
        # ШАГ 1. Создаём начальный список токенов
        # ============================================================

        # set(text) берёт все уникальные символы.
        #
        # Например:
        #
        # text = "banana"
        # set(text) -> {"b", "a", "n"}
        #
        # sorted(...) сортирует их по алфавиту:
        #
        # ["a", "b", "n"]
        tokens = sorted(set(text))

        # sequence — текущая последовательность токенов.
        #
        # В начале каждый символ является отдельным токеном.
        #
        # "banana" ->
        # ["b", "a", "n", "a", "n", "a"]
        sequence = list(text)

        # На случай повторного вызова fit() у того же объекта
        # очищаем список merge-операций.
        self.merges = []

        # ============================================================
        # ШАГ 2. Основной цикл обучения
        # ============================================================

        # Продолжаем создавать новые токены,
        # пока размер словаря меньше vocab_size.
        while len(tokens) < self.vocab_size:

            # Здесь будем хранить частоту каждой пары соседних токенов.
            #
            # Пример:
            #
            # {
            #     ("a", "n"): 2,
            #     ("n", "a"): 2,
            #     ("b", "a"): 1
            # }
            pair_counts = {}

            # ========================================================
            # ШАГ 2.1. Считаем частоты соседних пар
            # ========================================================

            # Проходим по последовательности до предпоследнего элемента,
            # потому что берём пару:
            #
            # sequence[i], sequence[i + 1]
            for i in range(len(sequence) - 1):
                # Берём пару соседних токенов.
                #
                # Например, для:
                #
                # ["b", "a", "n"]
                #
                # при i = 0:
                #
                # pair = ("b", "a")
                pair = (sequence[i], sequence[i + 1])

                # Увеличиваем счётчик этой пары.
                #
                # Если пары ещё нет в словаре, берём 0.
                pair_counts[pair] = pair_counts.get(pair, 0) + 1

            # Если пар нет, значит последовательность состоит из одного токена
            # или текст был пустой. Продолжать обучение невозможно.
            if not pair_counts:
                break

            # ========================================================
            # ШАГ 2.2. Выбираем самую частую пару
            # ========================================================

            # max(pair_counts, key=pair_counts.get)
            #
            # означает:
            #
            # "найди такой ключ в pair_counts,
            # у которого значение pair_counts[key] максимально".
            #
            # Если несколько пар имеют одинаковую частоту,
            # Python выберет первую встретившуюся пару,
            # так как dict сохраняет порядок вставки.
            best_pair = max(pair_counts, key=pair_counts.get)

            # Новый токен — это склейка двух частей пары.
            #
            # Например:
            #
            # ("a", "n") -> "an"
            #
            # ("an", "a") -> "ana"
            new_token = best_pair[0] + best_pair[1]

            # Добавляем новый токен в список всех токенов.
            tokens.append(new_token)

            # Сохраняем реальную merge-пару.
            #
            # Это обязательно нужно для encode().
            #
            # Во время кодирования мы должны применять merge
            # в том же порядке, в котором они были изучены.
            self.merges.append(best_pair)

            # ========================================================
            # ШАГ 2.3. Объединяем найденную пару в sequence
            # ========================================================

            # Сюда будем собирать новую последовательность токенов.
            new_sequence = []

            # Индекс текущего токена.
            i = 0

            while i < len(sequence):

                # Проверяем, есть ли справа ещё один токен
                # и совпадает ли текущая пара с best_pair.
                if (
                        i < len(sequence) - 1
                        and sequence[i] == best_pair[0]
                        and sequence[i + 1] == best_pair[1]
                ):
                    # Если нашли нужную пару,
                    # заменяем два токена одним новым.
                    #
                    # Например:
                    #
                    # ["a", "n"] -> ["an"]
                    new_sequence.append(new_token)

                    # Так как мы обработали сразу два токена,
                    # перескакиваем через оба.
                    i += 2
                else:
                    # Если пара не совпала,
                    # просто переносим текущий токен как есть.
                    new_sequence.append(sequence[i])

                    # Переходим к следующему токену.
                    i += 1

            # Обновляем sequence.
            #
            # Теперь в нём уже есть новый объединённый токен.
            sequence = new_sequence

        # ============================================================
        # ШАГ 3. Создаём словари id2token и token2id
        # ============================================================

        # id2token — словарь вида:
        #
        # {
        #     0: "a",
        #     1: "b",
        #     2: "n",
        #     3: "an"
        # }
        #
        # enumerate(tokens) выдаёт пары:
        #
        # 0, "a"
        # 1, "b"
        # 2, "n"
        # 3, "an"
        self.id2token = {
            token_id: token
            for token_id, token in enumerate(tokens)
        }

        # token2id — обратный словарь:
        #
        # {
        #     "a": 0,
        #     "b": 1,
        #     "n": 2,
        #     "an": 3
        # }
        self.token2id = {
            token: token_id
            for token_id, token in self.id2token.items()
        }

    def encode(self, text: str):
        """
        Кодирует текст в список ID токенов.

        Parameters
        ----------
        text : str
            Текст, который нужно закодировать.

        Returns
        -------
        list[int]
            Список числовых идентификаторов токенов.

        Как работает encode:

        1. Разбивает новый текст на символы.
        2. Берёт сохранённые merge-операции из self.merges.
        3. Применяет их строго в том же порядке,
           в котором они были изучены во время fit().
        4. Получившиеся токены переводит в ID через token2id.

        Почему нельзя просто идти по id2token и разбирать токены?

        Потому что составной токен может быть создан из двух других
        составных токенов.

        Например:

        ("ab", "cd") -> "abcd"

        Если смотреть только на строку "abcd",
        невозможно надёжно понять, что она была создана именно
        из "ab" и "cd", а не из "abc" и "d".

        Поэтому правильный способ — хранить реальные merge-пары
        во время fit().
        """

        # Начинаем с символов, как и во время обучения.
        #
        # Например:
        #
        # "banana" ->
        # ["b", "a", "n", "a", "n", "a"]
        sequence = list(text)

        # ============================================================
        # Применяем все merge-операции в порядке обучения
        # ============================================================

        # self.merges содержит пары вида:
        #
        # [
        #     ("a", "n"),
        #     ("an", "a"),
        #     ("b", "ana")
        # ]
        #
        # Очень важно не менять порядок.
        for left, right in self.merges:

            # Новый токен получается склейкой двух токенов пары.
            #
            # Например:
            #
            # left = "a"
            # right = "n"
            # new_token = "an"
            new_token = left + right

            # Сюда будем собирать последовательность
            # после применения текущего merge.
            new_sequence = []

            i = 0

            while i < len(sequence):

                # Проверяем, совпадает ли текущая соседняя пара
                # с merge-парой, которую сейчас применяем.
                if (
                        i < len(sequence) - 1
                        and sequence[i] == left
                        and sequence[i + 1] == right
                ):
                    # Если совпала — заменяем пару одним новым токеном.
                    new_sequence.append(new_token)

                    # Перепрыгиваем через два обработанных токена.
                    i += 2
                else:
                    # Если не совпала — оставляем токен без изменений.
                    new_sequence.append(sequence[i])
                    i += 1

            # После применения одного merge обновляем sequence.
            sequence = new_sequence

        # ============================================================
        # Переводим итоговые токены в их ID
        # ============================================================

        # Например:
        #
        # sequence = ["banana"]
        #
        # token2id = {
        #     "banana": 5
        # }
        #
        # результат:
        #
        # [5]
        return [
            self.token2id[token]
            for token in sequence
        ]

    def decode(self, token_ids: list[int]):
        """
        Декодирует список ID токенов обратно в строку.

        Parameters
        ----------
        token_ids : list[int]
            Список идентификаторов токенов.

            Например:

            [1, 3, 4]

        Returns
        -------
        str
            Восстановленная строка текста.

        Как работает метод:

        1. Берёт каждый token_id.
        2. Находит соответствующий токен через self.id2token.
        3. Собирает все текстовые токены в список.
        4. Склеивает список в одну строку.

        Пример
        -------

        Пусть словарь такой:

        self.id2token = {
            0: "a",
            1: "b",
            2: "n",
            3: "an",
            4: "ana"
        }

        Тогда:

        decode([1, 3, 4])

        означает:

        [1, 3, 4]
          ↓
        ["b", "an", "ana"]
          ↓
        "banana"
        """

        # ============================================================
        # ШАГ 1. Преобразуем ID токенов в текстовые токены
        # ============================================================

        # Сюда будем складывать текстовые представления токенов.
        #
        # Например:
        #
        # ["b", "an", "ana"]
        tokens = []

        # Проходим по каждому идентификатору токена.
        for token_id in token_ids:
            # Находим текстовый токен по ID.
            #
            # Например:
            #
            # 3 -> "an"
            token = self.id2token[token_id]

            # Добавляем токен в список.
            tokens.append(token)

        # ============================================================
        # ШАГ 2. Склеиваем токены обратно в строку
        # ============================================================

        # "".join(tokens)
        #
        # означает:
        #
        # "склей список строк без разделителя"
        #
        # Например:
        #
        # ["b", "an", "ana"]
        #
        # ->
        #
        # "banana"
        return "".join(tokens)

    def save(self, filename):
        """
        Сохраняет обученную модель BPE в файл с помощью dill.

        Метод сериализует текущий объект (включая все его атрибуты,
        такие как id2token, token2id и merges) в бинарный файл.

        Parameters
        ----------
        filename : str
            Имя файла (или путь к файлу), куда будет сохранён объект.

        Примеры
        --------
        >>> bpe = BPE(vocab_size=100)
        >>> bpe.fit("some text data")
        >>> bpe.save("bpe_model.dill")
        Объект сохранён в bpe_model.dill
        """
        with open(filename, 'wb') as f:
            dill.dump(self, f)
        print(f"Объект сохранён в {filename}")

    @classmethod
    def load(cls, filename):
        """
        Загружает обученную модель BPE из файла.

        Это метод класса, который создаёт и возвращает новый экземпляр BPE,
        восстановленный из сериализованного состояния.

        Parameters
        ----------
        filename : str
            Имя файла (или путь к файлу), из которого будет загружен объект.

        Returns
        -------
        BPE
            Новый экземпляр класса BPE, восстановленный из файла.

        Примеры
        --------
        >>> bpe = BPE.load("bpe_model.dill")
        Объект загружен из bpe_model.dill
        >>> bpe.encode("new text")
        [1, 2, 3]
        """
        with open(filename, 'rb') as f:
            obj = dill.load(f)

        print(f"Объект загружен из {filename}")
        return obj
