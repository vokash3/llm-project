class BPE:
    """
    Реализация алгоритма Byte Pair Encoding (BPE).

    BPE — это алгоритм токенизации, который:
    1. Начинает с базовых токенов (символов).
    2. Находит самые частые пары соседних токенов.
    3. Объединяет их в новый токен.
    4. Повторяет процесс, пока размер словаря
       не достигнет vocab_size.

    Пример:

    text = "banana"

    Изначально:
    ['b', 'a', 'n', 'a', 'n', 'a']

    Частые пары:
    ('a', 'n') -> 2 раза
    ('n', 'a') -> 2 раза

    Берём первую по порядку появления.

    После merge:
    ['b', 'an', 'an', 'a']

    Затем продолжаем обучение.

    После обучения формируется:
    - id2token: {id -> token}
    - token2id: {token -> id}
    """

    def __init__(self, vocab_size: int):
        """
        Конструктор класса.

        Parameters
        ----------
        vocab_size : int
            Максимальный размер словаря токенов.
        """

        # Размер словаря, до которого обучаем BPE
        self.vocab_size = vocab_size

        # Словарь вида:
        # {0: 'a', 1: 'b', 2: 'c'}
        self.id2token = {}

        # Обратный словарь:
        # {'a': 0, 'b': 1, 'c': 2}
        self.token2id = {}

    def fit(self, text: str):
        """
        Обучение BPE на текстовом корпусе.

        Что происходит:
        1. Из текста берутся уникальные символы.
        2. Они становятся начальными токенами.
        3. Далее ищется самая частая пара соседних токенов.
        4. Пара объединяется в новый токен.
        5. Процесс повторяется, пока размер словаря
           не достигнет vocab_size.

        Parameters
        ----------
        text : str
            Корпус текста для обучения.

        Example
        -------
        text = "banana"

        Старт:
        ['b', 'a', 'n', 'a', 'n', 'a']

        После merge('a', 'n'):
        ['b', 'an', 'an', 'a']
        """

        # =====================================================
        # ШАГ 1. Создаём базовый словарь
        # =====================================================

        # Получаем уникальные символы текста
        # set(text) -> {'b', 'a', 'n'}
        #
        # Сортируем:
        # ['a', 'b', 'n']
        tokens = sorted(set(text))

        # Разбиваем текст на отдельные символы
        #
        # "banana" ->
        # ['b', 'a', 'n', 'a', 'n', 'a']
        sequence = list(text)

        # =====================================================
        # ШАГ 2. Основной цикл обучения BPE
        # =====================================================

        # Обучаемся, пока размер словаря
        # меньше заданного vocab_size
        while len(tokens) < self.vocab_size:

            # Словарь частот пар:
            #
            # {
            #   ('a', 'n'): 2,
            #   ('n', 'a'): 2,
            #   ('b', 'a'): 1
            # }
            pair_counts = {}

            # ==========================================
            # Подсчёт частот всех соседних пар
            # ==========================================

            for i in range(len(sequence) - 1):
                # Получаем пару соседних токенов
                #
                # ['b', 'a', 'n']
                # i=0 -> ('b', 'a')
                pair = (sequence[i], sequence[i + 1])

                # Увеличиваем счётчик
                pair_counts[pair] = pair_counts.get(pair, 0) + 1

            # Если пар больше нет — завершаем
            if not pair_counts:
                break

            # ==========================================
            # Ищем самую частую пару
            # ==========================================

            # max(dict, key=dict.get)
            #
            # Вернёт пару с максимальной частотой.
            #
            # Важно:
            # Python dict сохраняет порядок вставки.
            # Поэтому если есть несколько одинаковых
            # по частоте пар — возьмётся первая.
            best_pair = max(pair_counts, key=pair_counts.get)

            # Создаём новый токен:
            #
            # ('a', 'n') -> 'an'
            new_token = best_pair[0] + best_pair[1]

            # Добавляем в словарь токенов
            tokens.append(new_token)

            # ==========================================
            # Делаем merge в последовательности
            # ==========================================

            new_sequence = []

            i = 0

            while i < len(sequence):

                # Проверяем:
                # встречается ли нужная пара подряд
                if (
                        i < len(sequence) - 1
                        and sequence[i] == best_pair[0]
                        and sequence[i + 1] == best_pair[1]
                ):
                    # Заменяем пару новым токеном
                    #
                    # ['a', 'n'] -> 'an'
                    new_sequence.append(new_token)

                    # Пропускаем 2 позиции
                    i += 2
                else:
                    # Иначе просто копируем токен
                    new_sequence.append(sequence[i])
                    i += 1

            # Обновляем последовательность
            sequence = new_sequence

        # =====================================================
        # ШАГ 3. Создание словарей токенов
        # =====================================================

        # id -> token
        #
        # {
        #   0: 'a',
        #   1: 'b',
        #   2: 'n',
        #   3: 'an'
        # }
        self.id2token = {
            token_id: token
            for token_id, token in enumerate(tokens)
        }

        # token -> id
        #
        # {
        #   'a': 0,
        #   'b': 1,
        #   'n': 2,
        #   'an': 3
        # }
        self.token2id = {
            token: token_id
            for token_id, token in self.id2token.items()
        }

    def encode(self, text: str):
        """
        Кодирует текст в последовательность id.

        Логика:
        Мы повторяем те же merge-операции,
        которые были изучены в fit().

        Например:

        Если во время обучения появились:

        'an'
        'ana'
        'ban'

        Тогда encode будет применять их
        в том же порядке.

        Parameters
        ----------
        text : str
            Текст для кодирования.

        Returns
        -------
        list[int]
            Список id токенов.

        Example
        -------
        bpe.fit("banana")

        bpe.encode("banana")
        -> [5]
        """

        # Разбиваем текст в символы
        sequence = list(text)

        # =====================================================
        # Повторяем merge в порядке обучения
        # =====================================================

        # Идём по токенам словаря
        for token_id in range(len(self.id2token)):

            token = self.id2token[token_id]

            # Базовые символы пропускаем
            #
            # 'a'
            # 'b'
            # 'n'
            if len(token) < 2:
                continue

            # Разделяем токен на:
            #
            # 'an'
            # ->
            # left='a'
            # right='n'
            #
            # 'banana'
            # ->
            # left='banan'
            # right='a'
            left = token[:-1]
            right = token[-1]

            new_sequence = []
            i = 0

            while i < len(sequence):

                # Если нашли нужную пару —
                # объединяем её
                if (
                        i < len(sequence) - 1
                        and sequence[i] == left
                        and sequence[i + 1] == right
                ):
                    new_sequence.append(token)

                    # Перескакиваем через пару
                    i += 2
                else:
                    new_sequence.append(sequence[i])
                    i += 1

            sequence = new_sequence

        # =====================================================
        # Переводим токены в id
        # =====================================================

        return [
            self.token2id[token]
            for token in sequence
        ]
